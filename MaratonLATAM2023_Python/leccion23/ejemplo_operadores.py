# Operadores _ Herramientas de manipulacion de datos

# Operador de suma(+).
print("suma.")
print(2+2)

# Operador de Exponenciación(**).
print("Exponenciacion.")
print(2 ** 3)
print(2 ** 3.)
print(2. ** 3)
print(2. ** 3.)

# Operador de Multiplicacion(*).
print("Multiplicacion.")
print(2 * 3)
print(2 * 3.)
print(2. * 3)
print(2. * 3.)

# Operador de Division simple(/).
print("Division simple.")
print(6 / 3)
print(6 / 3.)
print(6. / 3)
print(6. / 3.)

# Operador con Division entera(//).
print("Division entera.")
print(6 // 3)
print(6 // 3.)
print(6. // 3)
print(6. // 3.)

# Operador con Residuo (módulo)-(%).
print("Residuo.")
print(14 % 4)

# Operadores Unarios(+,-).
print("Operador con suma.")
print(-4 + 4)
print(-4. + 8)

print("Operador Unario con resta.") 
print(-4 - 4)
print(4. - 8)
print(-1.1)
