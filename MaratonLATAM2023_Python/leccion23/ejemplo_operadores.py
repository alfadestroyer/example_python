# Operador de Division simple(/).
print("Division simple.")
print(6 / 3)
print(6 / 3.)
print(6. / 3)
print(6. / 3.)

# Operador con Division entera(//).
print("Division entera.")
print(6 // 3)
print(6 // 3.)
print(6. // 3)
print(6. // 3.)