# Operadores _ Herramientas de manipulacion de datos

# Operador de suma(+).
print("suma.")
print(2+2)

# Operador de Exponenciación(**).
print("Exponenciacion.")
print(2 ** 3)
print(2 ** 3.)
print(2. ** 3)
print(2. ** 3.)

# Operador de Multiplicacion(*).
print("Multiplicacion.")
print(2 * 3)
print(2 * 3.)
print(2. * 3)
print(2. * 3.)

# Operador de Division simple(/).
print("Division simple.")
print(6 / 3)
print(6 / 3.)
print(6. / 3)
print(6. / 3.)

# Operador con Division entera(//).
print("Division entera.")
print(6 // 3)
print(6 // 3.)
print(6. // 3)
print(6. // 3.)

# Operador con Residuo (módulo)-(%).
print("Residuo.")
print(14 % 4)

# Operadores Unarios(+,-).
print("Operador con suma.")
print(-4 + 4)
print(-4. + 8)

print("Operador Unario con resta.") 
print(-4 - 4)
print(4. - 8)
print(-1.1)

# Operadores y Parentesis.
print("Operadores y Parentesis.")
print((5 * ((25 % 13) + 100) / (2 * 13)) // 2)

# Operadores Abreviados.
x = input("ingrese un numero: ")
x = x * 2
print(x)

# ejemplo 2.
sheep = int(input("ingrese un numero: "))
sheep = sheep + 1
print(sheep)

# ejemplo 3.
x = input("ingrese primer numero: ")
sheep = int(input("ingrese segundo numero: "))
x *= 2
sheep += 1
print(x , sheep)
